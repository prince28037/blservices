This agreement is made and entered into effective this by and
{{ $name }}
between Samaritus Capital Inc. a New York Corporation (the “Company”), and the undersigned
Referral Partner (“Referral Partner”).
WHEREAS, the Company is engaged in the business of providing commercial financing solutions
itself as well as through an aggregate network (the “Network”) of funding and payment processing
companies or organizations that may purchase all of the merchant’s rights, title and interests in
and to a specified amount of the merchant’s future sales (“Future Receivables”) from qualifying
merchants (the “Program”) in their sole and absolute discretion and may implement payment
processing software and integrations and related products as mentioned in the aggregate
network’s (the “Network”) respective contracts and terms; and WHEREAS the Company is paid
commissions by referring any merchant by which the Company or its undersigned Referral
Partners engage with (a “Referred Merchant”) to the Company, or to any company or organization
with the Network.
WHEREAS, Referral Partner is engaged in business as a leasing, funding advisor, association, or
independent sales organization; consultant or broker or service provider that works with
small business owners on a regular basis; and
WHEREAS, the Company and Referral Partner desire to enter into an arrangement for Referral
Partner to refer merchants to the Company as set forth in this Agreement.
NOW, THEREFORE, for good and valuable consideration, the receipt and sufficiency of which are
hereby acknowledged, the parties hereto, intending to be legally bound, hereby agree as follows.
1) Referral Process; Referral Fee
If the Company and Network accepts the application of any merchant that includes a statement
that Referral Partner is the referring organization (a “Referred Merchant”) and, in its sole
discretion, enters into an Agreement to purchase Future Receivables from such Referred
Merchant (a “Purchase Agreement”), then pursuant to amount paid under the Purchase
Agreement (the “Purchase Price”) or implements payment processing software and
integrations or related products as mentioned in the Network’s respective contracts and terms,
Referral Partner shall be entitled to receive Revenue as detailed in the schedule A of this
agreement. Notwithstanding anything herein to the contrary, the Company and the Network shall
be entitled to accept or reject any application by a Referred Merchant in its sole discretion and
shall have no obligation to accept any application of any Referred Merchant. No Purchase
